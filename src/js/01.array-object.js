// Array
let sampleArray = ["zeroth", "first", "second"];

sampleArray.push("third");

console.log(`[${sampleArray[0]}, ${sampleArray[1]}, ${sampleArray[2]}, ${sampleArray[3]}]`)


// Object
let sampleObject = {
	zeroth: "0",
	first: "1",
	second: "2"
}

sampleObject.third = "3";

console.log(`{zeroth: ${sampleObject.zeroth}, first: ${sampleObject.first}, second: ${sampleObject.second}, third: ${sampleObject.third}}`);

